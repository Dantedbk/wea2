def home(request):
    today = datetime.date.today()
    json_file_path = Path(__file__).resolve().parent.parent / 'static' / 'citas.json'
    with open(json_file_path, 'r') as file:
        data = json.load(file)
        quotes = data
        phrase_index = today.toordinal() % len(quotes)
        selected_phrase = quotes[phrase_index]

    return render(request, 'store/home.html', {'selected_phrase': selected_phrase})


